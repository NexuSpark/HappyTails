class Animal {
    constructor(name, age, weight, color, isVaccinated, breed, furType) {
        this.name = name;
        this.age = age;
        this.weight = weight;
        this.color = color;
        this.isVaccinated = isVaccinated;
        this.breed = breed;
        this.furType = furType;
    }
}

export default Animal;